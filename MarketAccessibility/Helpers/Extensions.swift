import UIKit

extension UIColor {
    struct App {
        static var maincolor: UIColor { return #colorLiteral(red: 0.2509803922, green: 0.5490196078, blue: 0.6, alpha: 1) }
        static var segmentedSelected: UIColor  { return #colorLiteral(red: 1.0, green: 1.0, blue: 1.0, alpha: 1.0) }
        static var segmentedUnselected: UIColor { return #colorLiteral(red: 0, green: 0, blue: 0, alpha: 1) }
        static var black: UIColor { return #colorLiteral(red: 0, green: 0, blue: 0, alpha: 1) }
        static var white: UIColor { return #colorLiteral(red: 1, green: 1, blue: 1, alpha: 1) }
    }
}
